then echo "One"
fi
if [ $n -eq 2 ]
then echo "Two"
fi
if [ $n -eq 3 ]
then echo "Three"
fi
if [ $n -eq 4 ]
then echo "Four"
fi
if [ $n -eq 5 ]
then echo "Five"
fi
if [ $n -eq 6 ]
then echo "Six"
fi
if [ $n -eq 7 ]
then echo "Seven"
fi
if [ $n -eq 8 ]
